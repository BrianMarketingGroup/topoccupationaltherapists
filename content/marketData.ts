/** O(n) lookup for exact market matches, e.g. from a locations[] entry. */
export function getMarket(city: string, state: string): Market | undefined {
  const c = city.trim().toLowerCase();
  const s = state.trim().toLowerCase();
  return MARKETS.find((m) => m.city.toLowerCase() === c && m.state.toLowerCase() === s);
}

/** Great-circle distance in miles between two lat/lng points. */
function haversineMiles(a: { lat: number; lng: number }, b: { lat: number; lng: number }): number {
  const R = 3958.8; // Earth radius in miles
  const toRad = (deg: number) => (deg * Math.PI) / 180;
  const dLat = toRad(b.lat - a.lat);
  const dLng = toRad(b.lng - a.lng);
  const lat1 = toRad(a.lat);
  const lat2 = toRad(b.lat);
  const h =
    Math.sin(dLat / 2) ** 2 + Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.asin(Math.sqrt(h));
}

/** Nearest N markets to a given market, excluding itself, sorted by distance ascending. */
export function getNearbyMarkets(
  city: string,
  state: string,
  limit = 3,
): (Market & { distanceMiles: number })[] {
  const origin = getMarket(city, state);
  if (!origin) return [];
  return MARKETS.filter((m) => !(m.city === origin.city && m.state === origin.state))
    .map((m) => ({ ...m, distanceMiles: haversineMiles(origin, m) }))
    .sort((a, b) => a.distanceMiles - b.distanceMiles)
    .slice(0, limit);
}

/** Largest N markets by population, for the pre-search "popular markets" seed grid. */
export function getPopularMarkets(limit = 12): Market[] {
  return [...MARKETS].sort((a, b) => b.population - a.population).slice(0, limit);
}
